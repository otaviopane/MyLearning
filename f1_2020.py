class Driver:

    def __init__(self, name, last_name, number, team, nationality, points, age, champions, victories, poles, podiums, fastest):
        print("Creating Driver: {} ... ".format(name))
        self.name = name
        self.last_name = last_name
        self.number = number
        self.team = team
        self.nationality = nationality
        self.points = points
        self.age = age
        self.champions = champions
        self.victories = victories
        self.poles = poles
        self.podiums = podiums
        self.fastest = fastest
